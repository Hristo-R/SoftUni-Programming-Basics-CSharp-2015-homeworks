using System;

class FloatOrDouble
{
    static void Main()
    {
        double doubleNum1 = 34.567839023;
        Console.WriteLine("The value: {0} can be assigned to a variable of type \"double\"", doubleNum1);

        float floadNum1 = 12.345f;
        Console.WriteLine("The value:       {0} can be assigned to a variable of type \"float\"", floadNum1);

        double doubleNum2 = 8923.1234857;
        Console.WriteLine("The value: {0} can be assigned to a variable of type \"double\"", doubleNum2);

        float floadNum2 = 3456.091f;
        Console.WriteLine("The value:     {0} can be assigned to a variable of type \"float\"", floadNum2);
    }
}